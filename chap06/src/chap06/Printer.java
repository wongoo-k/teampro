package chap06;

public class Printer {
	void println(int num) {
		System.out.println(num);
	}
	
	void println(boolean tf) {
		System.out.println(tf);
	}
	
	void println(double num) {
		System.out.println(num);
	}
	
	void println(float num) {
		System.out.println(num);
	}
	
	void println(String str) {
		System.out.println(str);
	}	
		
}
